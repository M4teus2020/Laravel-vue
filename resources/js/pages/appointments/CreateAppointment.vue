<script setup>
import axios from "axios";
import { ref, onMounted, reactive } from "vue";
import { useRouter } from "vue-router";
import { UseToastr } from "../../toastr";

const toastr = UseToastr()
const router = useRouter()
const form = reactive({
    title: '',
    client_id: '',
    start_date: '',
    start_time: '',
    description: '',
})

const handleSubmit = () => {
    axios.post('/api/appointments/create', form)
        .then(res => {
            toastr.success('Appointment created successfully!')
            router.push('/admin/appointments')
        })
        .catch(err => {
            console.log(err)
        })
}

const clients = ref([]);
const GetClients = () => {
    axios.get("/api/clients")
        .then((response) => {
            clients.value = response.data;
        })
        .catch((error) => {
            console.log(error);
        });
}

onMounted(() => {
    GetClients();
})

</script>
<template>
    <div class="content-header">
        <div class="container-fluid">
            <div class="row mb-2">
                <div class="col-sm-6">
                    <h1 class="m-0">Create Appointment</h1>
                </div>
                <div class="col-sm-6">
                    <ol class="breadcrumb float-sm-right">
                        <li class="breadcrumb-item">
                            <router-link to="/admin/dashboard">Home</router-link>
                        </li>
                        <li class="breadcrumb-item">
                            <router-link to="/admin/appointments">Appointments</router-link>
                        </li>
                        <li class="breadcrumb-item active">Create</li>
                    </ol>
                </div>
            </div>
        </div>
    </div>

    <div class="content">
        <div class="container-fluid">
            <div class="row">
                <div class="col-lg-12">
                    <div class="card">
                        <div class="card-body">
                            <form @submit.prevent="$event => handleSubmit()">
                                <div class="row">
                                    <div class="col-md-6">
                                        <div class="form-group">
                                            <label for="title">Title</label>
                                            <input v-model="form.title" type="text" class="form-control" id="title"
                                                placeholder="Enter Title">
                                        </div>
                                    </div>
                                    <div class="col-md-6">
                                        <div class="form-group">
                                            <label for="client">Client Name</label>
                                            <select v-model="form.client_id" id="client" class="form-control">
                                                <option value="" disabled selected>Select a Client: </option>
                                                <option v-for="client in clients" :value="client.id">{{ client.full_name }}
                                                </option>
                                            </select>
                                        </div>
                                    </div>
                                </div>
                                <div class="row">
                                    <div class="col-md-6">
                                        <div class="form-group">
                                            <label for="date">Appointment Date</label>
                                            <input v-model="form.start_date" type="date" class="form-control" id="date">
                                        </div>
                                    </div>
                                    <div class="col-md-6">
                                        <div class="form-group">
                                            <label for="time">Appointment Time</label>
                                            <input v-model="form.start_time" type="time" class="form-control" id="time">
                                        </div>
                                    </div>
                                </div>
                                <div class="form-group">
                                    <label for="description">Description</label>
                                    <textarea v-model="form.description" class="form-control" id="description" rows="3"
                                        placeholder="Enter Description"></textarea>
                                </div>
                                <button type="submit" class="btn btn-primary">Submit</button>
                            </form>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </div>
</template>